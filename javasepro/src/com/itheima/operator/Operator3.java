package com.itheima.operator;

public class Operator3 {
    public static void main(String[] args) {
        int a = 10;
        int b = 2;
        b += a;
        System.out.println(b);
        b -= a;
        System.out.println(b);
        b *= a;
        System.out.println(b);
        b /= a;
        System.out.println(b);
        b %= a;
        System.out.println(b);
    }
}